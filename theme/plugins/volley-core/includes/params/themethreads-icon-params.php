function themethreads_get_icon( $atts = array(), $add_icon = false, $prefix = 'i_' ) {

	$atts = shortcode_atts( array(

		// Icon
		$prefix . 'add_icon'         => false,
		$prefix . 'type'             => 'fontawesome',
		$prefix . 'icon_fontawesome' => false,
		$prefix . 'icon_linea'       => false,
		$prefix . 'icon_animated'         => false,
		$prefix . 'icon_image'       => false,

		//Custom icons
		$prefix . 'icon_themethreads_custom_icons' => false,

		// Style
		$prefix . 'color'            => false,
		$prefix . 'hcolor'           => false,
		$prefix . 'align'            => false,
		$prefix . 'size'             => false

	), $atts );

	if( $add_icon && 'true' !== $atts[ $prefix . 'add_icon' ] ) {
		return '';
	}

	if( 'image' === $atts[ $prefix . 'type' ] ) {
		$type = $atts[ $prefix . 'type' ];
		$image_url = isset( $atts[ $prefix . 'icon_' . $type ] ) ? wp_get_attachment_url( $atts[ $prefix . 'icon_' . $type ] ) : '';
		
		return array(
			'type' => $type,
			'src' => $image_url
		);
	}
	elseif( 'animated' === $atts[ $prefix . 'type' ] ) {
		$type = $atts[ $prefix . 'type' ];
		$icon = isset( $atts[ $prefix . 'icon_' . $type ] ) ? esc_attr( $atts[ $prefix . 'icon_' . $type ] ) : 'animated-arrows_anticlockwise';
		$svg  = str_replace( 'animated-', '', $icon );		

		$svg_src = LD_ADDONS_URL . 'assets/vendors/animated-icons/svg/' . $svg . '.svg';
		
		return array(
			'type' => $type,
			'src' => $svg_src,
			'class' => $icon,
		);
	}
	else {
		
		$type = $atts[ $prefix . 'type' ];
		$icon_class = isset( $atts[ $prefix . 'icon_' . $type ] ) ? esc_attr( $atts[ $prefix . 'icon_' . $type ] ) : 'fa fa-adjust';
		$icon_color = isset( $atts[ $prefix . 'color' ] ) ? $atts[ $prefix . 'color' ] : '';
		$icon_hcolor = isset( $atts[ $prefix . 'hcolor' ] ) ? $atts[ $prefix . 'hcolor' ] : '';
		$icon_align = isset( $atts[ $prefix . 'align' ] ) ? $atts[ $prefix . 'align' ] : 'left';
		$icon_size  = isset( $atts[ $prefix . 'size' ] ) ? $atts[ $prefix . 'size' ] : 'size';

		vc_icon_element_fonts_enqueue( $type );
	
		return array(
			'type'	=> $type,
			'color'	=> $icon_color,
			'hcolor' => $icon_hcolor,
			'icon'	=> $icon_class,
			'align'	=> $icon_align,
			'size'	=> $icon_size
		);
	}

}

function themethreads_get_icon_params( $add_icon = false, $group = '', $fonts = 'all', $remove = array(), $prefix = 'i_', $dependency = array() ) {

	$font_hash = array(
		'fontawesome'   => esc_html__( 'Font Awesome', 'volley-core' ),
		'linea'         => esc_html__( 'ThemeThreads', 'volley-core' ),
		'animated'      => esc_html__( 'Animated', 'volley-core' ),
		'image'    => esc_html__( 'Image icon', 'volley-core' ),
	);
	
	//Add custom icons fonts
	$font_hash = apply_filters( 'themethreads_custom_icons_font', $font_hash );

	$settings = array();

	// Add icon field
	if( $add_icon && 'manual' !== $add_icon ) {
		$settings[] = array(
            'type'       => 'checkbox',
            'param_name' => $prefix . 'add_icon',
            'heading'    => esc_html__( 'Add icon?', 'volley-core' ),
            'description' => esc_html__( 'Will enable icon on element', 'volley-core' ),
		);
	}

// Iconpicker field
	$add_these = $add_these_select = array();
	$add_these_select[esc_html__( 'Select icon font.', 'volley-core' )] = '';

	if( 'all' == $fonts ) {
		$fonts = array_keys( $font_hash );
	}

	foreach( $fonts as $font ) {

		$add_these_select[ $font_hash[$font] ] = $font;
		
		if( 'image' === $font ) {
			
			
			$add_these[] = array(
				'type'        => 'attach_image',
				'param_name'  => $prefix . 'icon_' . $font,
				'heading'     => esc_html__( 'Image', 'volley-core' ),
				'dependency'  => array(
					'element' => $prefix . 'type',
					'value'   => $font
				),
				'group' => $group
			);
		}
		else {			
	
			$add_these[] = array(
				'type'       => 'iconpicker',
				'param_name' => $prefix . 'icon_' . $font,
				'heading'    => esc_html__( 'Icon', 'volley-core' ),
				'description' => esc_html__( 'Select icon from library.', 'volley-core' ),
				'settings' => array(
					'emptyIcon'    => false,
					'type'         => $font,
					'iconsPerPage' => 4000
				),
				'dependency' => array(
					'element' => $prefix . 'type',
					'value'   => $font
				),
				'group' => $group
			);	
		}
	}

	// Icon library select field
	$settings[] = array(
		'type'       => 'dropdown',
		'param_name' => $prefix . 'type',
		'heading'    => esc_html__( 'Icon library', 'volley-core' ),
		'value'      => $add_these_select,
		'description' => esc_html__( 'Select icon library.', 'volley-core' ),
		'dependency'  => ! $add_icon ? $dependency : array(
			'element' => $prefix . 'add_icon',
            'value'   => 'true'
		),
		'group' => $group
	);

	$settings = array_merge( $settings, $add_these );

	// Icon color picker field
	if( ! in_array( 'color', $remove ) ) {
		$settings[] = array(
			'type'        => 'themethreads_colorpicker',
			'only_solid'  => true,
			'param_name'  => $prefix . 'color',
			'heading'     => esc_html__( 'Icon color', 'volley-core' ),
			'description' => esc_html__( 'Select icon color.', 'volley-core' ),
			'edit_field_class' => 'vc_col-sm-6',
			'dependency'  => ! $add_icon ? $dependency : array(
				'element' => $prefix . 'add_icon',
	            'value'   => 'true'
			),
			'group' => esc_html__( 'Design Options', 'volley-core' ),
		);
	}
	// Icon color picker field
	if( ! in_array( 'hcolor', $remove ) ) {
		$settings[] = array(
			'type'        => 'themethreads_colorpicker',
			'only_solid'  => true,
			'param_name'  => $prefix . 'hcolor',
			'heading'     => esc_html__( 'Icon hover color', 'volley-core' ),
			'description' => esc_html__( 'Pick icon hover color.', 'volley-core' ),
			'edit_field_class' => 'vc_col-sm-6',
			'dependency'  => ! $add_icon ? $dependency : array(
				'element' => $prefix . 'add_icon',
	            'value'   => 'true'
			),
			'group' => esc_html__( 'Design Options', 'volley-core' ),
		);
	}

	// Icon alignment field
	if( ! in_array( 'align', $remove ) ) {
		$settings[] = array(
			'type'       => 'dropdown',
			'param_name' => $prefix . 'align',
			'heading'    => esc_html__( 'Icon alignment', 'volley-core' ),
			'edit_field_class' => 'vc_col-sm-6',
			'value'      => array(
				esc_html__( 'Left', 'volley-core' )   => 'left',
				esc_html__( 'Right', 'volley-core' )  => 'right'
			),
			'description' => esc_html__( 'Select icon alignment.', 'volley-core' ),
			'group' => $group,
			'dependency'  => ! $add_icon ? $dependency : array(
				'element' => $prefix . 'add_icon',
	            'value'   => 'true'
			)
		);
	}
	
	// Icon alignment field
	if( ! in_array( 'size', $remove ) ) {
		$settings[] = array(
			'type'       => 'textfield',
			'param_name' => $prefix . 'size',
			'heading'    => esc_html__( 'Icon size', 'volley-core' ),
			'edit_field_class' => 'vc_col-sm-6',
			'description' => esc_html__( 'Set custom icon size with px, for ex. 24px', 'volley-core' ),
			'group' => $group,
			'dependency'  => ! $add_icon ? $dependency : array(
				'element' => $prefix . 'add_icon',
	            'value'   => 'true'
			)
		);
	}

	foreach( $settings as &$param ) {
		if( empty( $param['group'] ) ) {
			unset($param['group']);
		}
	}

	return $settings;
}

